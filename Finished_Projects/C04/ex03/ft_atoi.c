#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	posneg;
	int	res;

	i = 0;
	res = 0;
	posneg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			posneg = posneg * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	return (res = res * posneg);
}
