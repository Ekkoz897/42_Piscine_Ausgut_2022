#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	while (i <= (nb / 2))
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	num;

	num = nb;
	while (num < 2147483647 && !ft_is_prime(num))
		num++;
	return (num);
}

int	main(void)
{
	printf("%d", ft_find_next_prime(21474831));
}